package com.java.day14_WrapperClasses;

public class WrapperClassDemo {
	
	//int - Integer
	//byte - Byte
	//short - Short
	//long  - Long
	//double - Double
	//boolean - Boolean
	//float   - Float
	//char   - Character
	
	//What are Wrapper Classes?
	//pre-defined classes which can contain primitive data type. They wrap primitive data to Wrapper Class Object

	public static void main(String[] args) {
		
		int a = 10;
		System.out.println("The primitive data type value is :" + a);
		
		
		Integer b= 100;
		System.out.println("The Wrapper Class Object value is :" + b);
		
		//what is the difference between a and b ??

	}

}
